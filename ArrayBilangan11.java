public class ArrayBilangan11 {

    public static void main(String[] args) {
        
        int[] bil = {5, 13, -7, 17};
        
        // Menggunakan perulangan for untuk mencetak semua elemen array
        for (int i = 0; i <= 4; i++) {
            System.out.println(bil[i]);
        }
    }   
}
